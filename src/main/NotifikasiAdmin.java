    // METHOD UNTUK MEMUTAR AUDIO DARI PACKAGE voice
    private void playNotificationSound() {
        try {
            // Path menuju package voice di dalam src
            String path = "/voice/notifikasi.wav";
            InputStream audioSrc = getClass().getResourceAsStream(path);
            
            if (audioSrc != null) {
                InputStream bufferedIn = new BufferedInputStream(audioSrc);
                AudioInputStream audioStream = AudioSystem.getAudioInputStream(bufferedIn);
                Clip clip = AudioSystem.getClip();
                clip.open(audioStream);
                clip.start();
            } else {
                System.err.println("File audio tidak ditemukan di package voice!");
            }
        } catch (Exception e) {
            System.err.println("Gagal memutar suara: " + e.getMessage());
        }
    }

    private void loadNotifikasiAdmin() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Aktivitas Siswa");
        model.addColumn("Waktu");

        try {
            Connection conn = koneksi.KoneksiDB();
            
            // Query Admin: Mengambil pengajuan pinjaman DAN pengembalian yang masih status 'process'
            String sql = "SELECT CONCAT(s.name, ' (', s.nisn, ') mengajukan PEMINJAMAN') AS info, b.created_at " +
                         "FROM borrowings b JOIN students s ON b.student_nisn = s.nisn " +
                         "WHERE b.status = 'process' " +
                         "UNION " +
                         "SELECT CONCAT(s.name, ' mengembalikan buku (PROSES)') AS info, r.created_at " +
                         "FROM returns r JOIN borrowings b ON r.borrowing_id = b.id " +
                         "JOIN students s ON b.student_nisn = s.nisn " +
                         "WHERE r.status = 'process' " +
                         "ORDER BY created_at DESC";

            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("info"),
                    rs.getString("created_at")
                });
            }
            jTable1.setModel(model);

        } catch (Exception e) {
            System.err.println("Error Notif Admin: " + e.getMessage());
        }
    }
    
