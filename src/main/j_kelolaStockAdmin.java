    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String itemCode = txtKode.getText().trim();
        String bookId = txtIdBuku.getText().trim(); // ID buku tujuan

        if (itemCode.isEmpty() || bookId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lengkapi Kode Item dan ID Buku!");
            return;
        }

        // Validasi apakah bookId adalah angka
        int bookIdInt;
        try {
            bookIdInt = Integer.parseInt(bookId);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID Buku harus berupa angka!");
            return;
        }

        // Validasi apakah ID buku ada di database
        if (!isBookExists(bookIdInt)) {
            JOptionPane.showMessageDialog(this, "ID Buku '" + bookId + "' tidak ditemukan di database!");
            return;
        }

        String sql = "INSERT INTO book_items (code, book_id, status) VALUES (?, ?, 'available')";

        try (Connection conn = koneksi.KoneksiDB();
                PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setString(1, itemCode);
            pst.setInt(2, bookIdInt);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Item Stok Berhasil Ditambahkan!");
            loadStockData(); // Refresh tabel stok
            clearForm();
        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Gagal: Kode Item '" + itemCode + "' sudah terdaftar!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

