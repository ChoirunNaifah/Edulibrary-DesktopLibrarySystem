    private void loadStockData() {
        DefaultTableModel model = (DefaultTableModel) tblStok.getModel();
        model.setRowCount(0);

        // Query untuk mengambil detail setiap fisik buku
        String sql = "SELECT bi.code, bi.book_id, b.title, bi.status "
                + "FROM book_items bi "
                + "JOIN books b ON bi.book_id = b.id "
                + "ORDER BY b.title ASC";

        try (Connection conn = koneksi.KoneksiDB();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                Object[] row = {
                    rs.getString("code"), // Kode unik 
                    rs.getInt("book_id"), // ID Master Buku
                    rs.getString("title"), // Judul diambil dari tabel books
                    rs.getString("status") // status: available/borrowed
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat stok: " + e.getMessage());
        }
    }

    private boolean isBookExists(int bookId) {
        String sql = "SELECT COUNT(*) FROM books WHERE id = ?";

        try (Connection conn = koneksi.KoneksiDB();
                PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setInt(1, bookId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                return rs.getInt(1) > 0; // Jika count > 0 berarti buku ada
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat validasi ID buku: " + e.getMessage());
        }
        return false;
    }

