    private void button_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_cariActionPerformed
        // TODO add your handling code here:
        String keyword = txtCari.getText().trim();

        if (keyword.isEmpty()) {
            loadStockData(); // Panggil fungsi load standar jika input kosong
            return;
        }

        // Query untuk menarik fisik buku, metadata, dan cover-nya
        String sql = "SELECT bi.code, b.id, b.title, c.name AS category_name, "
                + "b.author, b.publisher, bi.status, b.cover "
                + "FROM book_items bi "
                + "JOIN books b ON bi.book_id = b.id "
                + "LEFT JOIN categories c ON b.category_id = c.id ";

        // Tentukan kondisi WHERE berdasarkan index ComboBox
        if (cmbCari.getSelectedIndex() == 0) { // Code dari book_items
            sql += "WHERE bi.code LIKE ?";
        } else if (cmbCari.getSelectedIndex() == 1) { // ID dari books
            sql += "WHERE b.id = ?";
        } else if (cmbCari.getSelectedIndex() == 2) { // Title dari books
            sql += "WHERE b.title LIKE ?";
        }

        try (Connection conn = koneksi.KoneksiDB();
                PreparedStatement pst = conn.prepareStatement(sql)) {

            // Set parameter sesuai tipe data
            if (cmbCari.getSelectedIndex() == 1) { // ID Buku (Integer)
                pst.setInt(1, Integer.parseInt(keyword));
            } else { // Code atau Title (String)
                pst.setString(1, "%" + keyword + "%");
            }

            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
            model.setRowCount(0);

            boolean ditemukan = false;
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("code"), // Kode Barcode Unit
                    rs.getInt("id"),
                    rs.getString("title"), // Judul Buku
                    rs.getString("category_name"), // Nama Kategori
                    "📷",
                    rs.getString("author"), // Penulis
                    rs.getString("publisher"), // Penerbit
                    rs.getString("status") // 'available' atau 'borrowed'
                });
                ditemukan = true;
            }

            if (!ditemukan) {
                JOptionPane.showMessageDialog(this, "Data stok tidak ditemukan!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Untuk pencarian ID Buku, masukkan angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Cari Stok: " + e.getMessage());
        }
    }//GEN-LAST:event_button_cariActionPerformed

