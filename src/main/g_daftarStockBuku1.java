    public void loadStockData() {
        DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
        model.setRowCount(0);

        // Query untuk menarik semua fisik buku beserta metadata dan nama kategorinya
        String sql = "SELECT bi.code, b.id, b.title, c.name AS category_name, "
                + "b.author, b.publisher, bi.status "
                + "FROM book_items bi "
                + "JOIN books b ON bi.book_id = b.id "
                + "LEFT JOIN categories c ON b.category_id = c.id "
                + "ORDER BY bi.code ASC";

        try (Connection conn = koneksi.KoneksiDB();
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("code"), // Kode Barcode Unit
                    rs.getInt("id"),
                    rs.getString("title"), // Judul Buku
                    rs.getString("category_name"), // Nama Kategori
                    "📷",
                    rs.getString("author"), // Penulis
                    rs.getString("publisher"), // Penerbit
                    rs.getString("status") // 'available' atau 'borrowed'
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data stok: " + e.getMessage());
        }
    }

    private void tampilkanCover(String kode) {
        try {
            Connection conn = koneksi.KoneksiDB();
            String sql = "SELECT cover FROM books WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, kode);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                byte[] imgBytes = rs.getBytes("cover");

                if (imgBytes != null) {
                    ImageIcon icon = new ImageIcon(imgBytes);
                    Image img = icon.getImage().getScaledInstance(
                            lblFoto.getWidth(),
                            lblFoto.getHeight(),
                            Image.SCALE_SMOOTH
                    );
                    lblFoto.setIcon(new ImageIcon(img));
                } else {
                    lblFoto.setIcon(null);
                }
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

