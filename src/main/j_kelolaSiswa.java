    private void btnCetakKartuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakKartuActionPerformed
        // TODO add your handling code here:
        if (txtNisn.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Masukkan NISN siswa terlebih dahulu!");
            return;
        }

        // Ambil NISN dari TextField
        String nisn = txtNisn.getText().trim();

        // Path ke file report jrxml (sesuaikan dengan lokasi Anda)
        String reportPath = "src/report/e_kartuSiswa.jrxml";

        // Query untuk mendapatkan data siswa
        String sql = "SELECT "
                + "students.`nisn` AS students_nisn, "
                + "students.`name` AS students_name, "
                + "students.`gender` AS students_gender, "
                + "students.`classroom` AS students_classroom, "
                + "students.`address` AS students_address, "
                + "students.`photo` AS students_photo "
                + "FROM `students` students "
                + "WHERE students.nisn = ?";

        Connection conn = null;
        try {
            // Eksekusi query
            conn = koneksi.KoneksiDB();
            pst = conn.prepareStatement(sql);
            pst.setString(1, nisn);
            ResultSet rs = pst.executeQuery();

            // Cek apakah ada data siswa yang ditemukan
            if (!rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(null, "NISN siswa tidak ditemukan!");
                return;
            }

            // Menampilkan laporan
            JRResultSetDataSource jrRS = new JRResultSetDataSource(rs);
            JasperReport jasperReport = JasperCompileManager.compileReport(reportPath);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, jrRS);

            // Menampilkan laporan dalam viewer
            JRViewer viewer = new JRViewer(jasperPrint);
            JDialog dialogViewer = new JDialog();
            dialogViewer.setTitle("Kartu Perpustakaan Siswa - NISN: " + nisn);
            dialogViewer.setAlwaysOnTop(true);
            dialogViewer.getContentPane().add(viewer);

            // Atur ukuran dialog (sesuaikan dengan ukuran kartu)
            Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
            dialogViewer.setBounds(0, 0, 800, 600); // Ukuran lebih kecil dari struk karena kartu lebih kecil
            dialogViewer.setLocationRelativeTo(null); // Tampilkan di tengah layar
            dialogViewer.setVisible(true);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal mengambil data siswa: " + e.getMessage());
            e.printStackTrace();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan kartu: " + e.getMessage());
            e.printStackTrace();
        } finally {
            // Tutup koneksi
            try {
                if (pst != null) {
                    pst.close();
                }
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnCetakKartuActionPerformed

