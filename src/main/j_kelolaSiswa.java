    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    String nisn = txtNisn.getText().trim();
    String name = txtNama.getText().trim();
    String gender = cmbGender.getSelectedItem().toString();
    String password = txtPassword.getText().trim();
    String classroom = cmbKelas.getSelectedItem().toString();
    String phone = txtNo.getText().trim();
    String address = txtAlamat.getText().trim();

    // Cek kosong
    if (nisn.isEmpty() || name.isEmpty() || password.isEmpty()
            || phone.isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Lengkapi semua data!");
        return;
    }

    // Validasi NISN harus 10 digit angka
    if (!nisn.matches("\\d{10}")) {
        JOptionPane.showMessageDialog(this, "NISN harus 10 digit angka!");
        return;
    }

    // Validasi No HP hanya angka
    if (!phone.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "Nomor HP hanya boleh angka!");
        return;
    }

    //  Validasi password minimal 6 karakter
    if (password.length() < 6) {
        JOptionPane.showMessageDialog(this, "Password minimal 6 karakter!");
        return;
    }

    try {
        Connection conn = koneksi.KoneksiDB();

        // Cek apakah NISN sudah ada
        String checkSql = "SELECT nisn FROM students WHERE nisn = ?";
        PreparedStatement checkPst = conn.prepareStatement(checkSql);
        checkPst.setString(1, nisn);
        ResultSet rs = checkPst.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "NISN sudah terdaftar!");
            return;
        }

        // INSERT DATA
        String sql = "INSERT INTO students (nisn, name, gender, password, classroom, phone_number, address, photo) "
                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, nisn);
        pst.setString(2, name);
        pst.setString(3, gender);
        pst.setString(4, password);
        pst.setString(5, classroom);
        pst.setString(6, phone);
        pst.setString(7, address);

        if (selectedImageFile != null) {
            FileInputStream fis = new FileInputStream(selectedImageFile);
            pst.setBinaryStream(8, fis, (int) selectedImageFile.length());
        } else {
            pst.setNull(8, java.sql.Types.BLOB);
        }

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data siswa berhasil disimpan!");

        pst.close();
        checkPst.close();
        conn.close();

        loadStudentData();
        clearForm();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
    } catch (FileNotFoundException ex) {
        Logger.getLogger(j_kelolaSiswa.class.getName()).log(Level.SEVERE, null, ex);
    
}
    }//GEN-LAST:event_btnSaveActionPerformed

