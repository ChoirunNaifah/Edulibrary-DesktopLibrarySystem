    private void loadStudentData() {
        String query = "SELECT * FROM students";

        try {
            Connection conn = koneksi.KoneksiDB();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            DefaultTableModel model = (DefaultTableModel) tblSiswa.getModel();
            model.setRowCount(0); // reset data tabel

            while (rs.next()) {
                Object[] row = new Object[8];
                row[0] = rs.getString("nisn");
                row[1] = rs.getString("name");
                row[2] = rs.getString("gender");
                row[3] = rs.getString("classroom");
                row[4] = rs.getString("phone_number");
                row[5] = rs.getString("address");
                row[6] = "📷";

                model.addRow(row);
            }

            rs.close();
            stmt.close();
            conn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void tampilkanFoto(String kode) {
        try {
            Connection conn = koneksi.KoneksiDB();
            String sql = "SELECT photo FROM students WHERE nisn = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, kode);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                byte[] imgBytes = rs.getBytes("photo");

                if (imgBytes != null) {
                    ImageIcon icon = new ImageIcon(imgBytes);
                    Image img = icon.getImage().getScaledInstance(
                            lblFoto.getWidth(),
                            lblFoto.getHeight(),
                            Image.SCALE_SMOOTH
                    );
                    lblFoto.setIcon(new ImageIcon(img));
                } else {
                    lblFoto.setIcon(null);
                }
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

