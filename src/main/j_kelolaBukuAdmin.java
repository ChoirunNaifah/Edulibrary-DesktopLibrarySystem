    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
                                           
    String idBuku = txtIdBuku.getText(); 
    String title = txtTitle.getText();
    String idKategori = txtIdKategori.getText();
    String jumlahHalaman = txtJumlahHalaman.getText();
    String author = txtAuthor.getText();
    String publisher = txtPenerbit.getText();
    String origin = txtAsal.getText();

    // Validasi Input
    if (idBuku.isEmpty() || title.isEmpty() || author.isEmpty() || idKategori.isEmpty()
            || jumlahHalaman.isEmpty() || publisher.isEmpty() || origin.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Lengkapi semua field!");
        return;
    }

    try (Connection conn = koneksi.KoneksiDB()) {
        PreparedStatement pst;
        String sql;

        if (selectedImageFile != null) {
            sql = "UPDATE books SET title=?, category_id=?, total_pages=?, author=?, publisher=?, origin=?, cover=? "
                    + "WHERE id=?"; 
            pst = conn.prepareStatement(sql);

            pst.setString(1, title);
            pst.setInt(2, Integer.parseInt(idKategori));
            pst.setInt(3, Integer.parseInt(jumlahHalaman));
            pst.setString(4, author);
            pst.setString(5, publisher);
            pst.setString(6, origin);
            
            // Parameter ke-7 adalah file gambar
            FileInputStream fis = new FileInputStream(selectedImageFile);
            pst.setBinaryStream(7, fis, (int) selectedImageFile.length());
            
            // Parameter ke-8 adalah ID Buku untuk WHERE clause
            pst.setString(8, idBuku);

        } else {
            // ----- UPDATE TANPA MENGUBAH COVER -----
            // Total ada 7 tanda tanya (?)
            sql = "UPDATE books SET title=?, category_id=?, total_pages=?, author=?, publisher=?, origin=? "
                    + "WHERE id=?";
            pst = conn.prepareStatement(sql);

            pst.setString(1, title);
            pst.setInt(2, Integer.parseInt(idKategori));
            pst.setInt(3, Integer.parseInt(jumlahHalaman));
            pst.setString(4, author);
            pst.setString(5, publisher);
            pst.setString(6, origin);
            
            // Parameter ke-7 adalah ID Buku
            pst.setString(7, idBuku);
        }

        int affected = pst.executeUpdate();
        if (affected > 0) {
            JOptionPane.showMessageDialog(this, "Data buku berhasil diupdate!");
            loadBookData();
            clearForm();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error Update: " + e.getMessage());
    }

    }//GEN-LAST:event_btnUpdateActionPerformed

