    private void loadBookData() {
        DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
        model.setRowCount(0);

        // Query untuk mengambil data buku sekaligus menghitung stok total dan stok available dari tabel book_items
        String sql = "SELECT b.id, b.title, c.name AS category, b.author, b.publisher, "
                + "b.origin, b.total_pages, "
                + "(SELECT COUNT(*) FROM book_items bi WHERE bi.book_id = b.id) AS total_stock, "
                + "(SELECT COUNT(*) FROM book_items bi WHERE bi.book_id = b.id AND bi.status = 'available') AS available_stock "
                + "FROM books b "
                + "LEFT JOIN categories c ON b.category_id = c.id";

        try {
            Connection conn = koneksi.KoneksiDB();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id"),
                    rs.getString("title"),
                    rs.getString("category"),
                    rs.getString("author"),
                    rs.getString("publisher"),
                    rs.getString("origin"),
                    rs.getInt("total_pages"),
                    "📷", // Placeholder untuk Cover (karena BLOB biasanya tidak ditampilkan langsung di tabel text)
                    rs.getInt("total_stock"), // Total stok dari semua book_items
                    rs.getInt("available_stock") // Stok yang tersedia (status available)
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

    private void tampilkanCover(String kode) {
        try {
            Connection conn = koneksi.KoneksiDB();
            String sql = "SELECT cover FROM books WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, kode);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                byte[] imgBytes = rs.getBytes("cover");

                if (imgBytes != null) {
                    ImageIcon icon = new ImageIcon(imgBytes);
                    Image img = icon.getImage().getScaledInstance(
                            lblFoto.getWidth(),
                            lblFoto.getHeight(),
                            Image.SCALE_SMOOTH
                    );
                    lblFoto.setIcon(new ImageIcon(img));
                } else {
                    lblFoto.setIcon(null);
                }
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

