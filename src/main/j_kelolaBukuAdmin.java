    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String title = txtTitle.getText();
        String author = txtAuthor.getText();
        String publisher = txtPenerbit.getText();
        String origin = txtAsal.getText();
        String pages = txtJumlahHalaman.getText();
        String categoryId = txtIdKategori.getText();

        if (title.isEmpty() || author.isEmpty() || pages.isEmpty() || categoryId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lengkapi data utama!");
            return;
        }

        // Tambahkan 'cover' ke query
        String sql = "INSERT INTO books (title, author, publisher, origin, total_pages, category_id, cover) VALUES (?, ?, ?, ?, ?, ?, ?)";

        try (Connection conn = koneksi.KoneksiDB();
                PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setString(1, title);
            pst.setString(2, author);
            pst.setString(3, publisher);
            pst.setString(4, origin);
            pst.setInt(5, Integer.parseInt(pages));
            pst.setInt(6, Integer.parseInt(categoryId));

            // Logika untuk menyimpan file gambar
            if (selectedImageFile != null) {
                FileInputStream fis = new FileInputStream(selectedImageFile);
                pst.setBinaryStream(7, fis, (int) selectedImageFile.length());
            } else {
                pst.setNull(7, java.sql.Types.BLOB);
            }

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Buku & Cover Berhasil Disimpan!");

            // Reset file setelah simpan agar tidak tertukar
            selectedImageFile = null;
            loadBookData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

