    private void btnCetakDataBukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakDataBukuActionPerformed
        // TODO add your handling code here:
        Connection conn = null;

        try {
            String reportPath = "src/report/f_dataBuku.jrxml";

            conn = koneksi.KoneksiDB();

            JasperReport jasperReport = JasperCompileManager.compileReport(reportPath);

            // Kirim parameter admin
            Map<String, Object> parameters = new HashMap<>();
            parameters.put("ADMIN_NAME", session_admin.nameAdmin);

            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parameters, conn);

            JRViewer viewer = new JRViewer(jasperPrint);
            JDialog dialog = new JDialog();
            dialog.setTitle("Laporan Inventaris Buku");
            dialog.setAlwaysOnTop(true);
            dialog.getContentPane().add(viewer);

            Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
            dialog.setBounds(0, 0, screenSize.width, screenSize.height);
            dialog.setLocationRelativeTo(null);
            dialog.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menampilkan laporan: " + e.getMessage());
            e.printStackTrace();
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnCetakDataBukuActionPerformed

