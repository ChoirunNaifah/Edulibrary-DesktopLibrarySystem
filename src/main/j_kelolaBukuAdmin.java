    private void button_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_cariActionPerformed
        // TODO add your handling code here:
        String keyword = txtCari.getText().trim();

        if (keyword.isEmpty()) {
            loadBookData(); // Jika kosong, tampilkan semua data
            return;
        }

        // subquery untuk menghitung stock secara real-time dari book_items
        String sqlBase = "SELECT b.id, b.title, c.name AS category, "
                + "b.author, b.publisher, b.origin, b.total_pages, "
                + "(SELECT COUNT(*) FROM book_items bi WHERE bi.book_id = b.id) AS total_stock, "
                + "(SELECT COUNT(*) FROM book_items bi WHERE bi.book_id = b.id AND bi.status = 'available') AS available_stock "
                + "FROM books b "
                + "LEFT JOIN categories c ON b.category_id = c.id ";

        String sqlCondition = "";

        // Menentukan kondisi berdasarkan pilihan combo box
        switch (cmbCari.getSelectedIndex()) {
            case 0: // ID Buku
                sqlCondition = "WHERE b.id = ?";
                break;
            case 1: // Judul Buku
                sqlCondition = "WHERE b.title LIKE ?";
                break;
            case 2: // Kategori
                sqlCondition = "WHERE c.name LIKE ?";
                break;
            case 3: // Penulis
                sqlCondition = "WHERE b.author LIKE ?";
                break;
            case 4: // Penerbit
                sqlCondition = "WHERE b.publisher LIKE ?";
                break;
            case 5: // Asal
                sqlCondition = "WHERE b.origin LIKE ?";
                break;
            default:
                sqlCondition = "";
                break;
        }

        try (Connection conn = koneksi.KoneksiDB();
                PreparedStatement pst = conn.prepareStatement(sqlBase + sqlCondition)) {

            // Set parameter berdasarkan tipe pencarian
            if (cmbCari.getSelectedIndex() == 0) { // ID Buku (integer)
                pst.setInt(1, Integer.parseInt(keyword));
            } else { // Pencarian teks (LIKE) untuk Judul, Kategori, Penulis, Penerbit, Asal
                pst.setString(1, "%" + keyword + "%");
            }

            ResultSet rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
            model.setRowCount(0);

            boolean ditemukan = false;
            while (rs.next()) {
                Object[] row = {
                    rs.getInt("id"), // ID Buku
                    rs.getString("title"), // Judul Buku
                    rs.getString("category"), // Kategori
                    rs.getString("author"), // Penulis
                    rs.getString("publisher"), // Penerbit
                    rs.getString("origin"), // Asal
                    rs.getInt("total_pages"), // Halaman
                    "📷", // Cover
                    rs.getInt("total_stock"), // Stock (total semua book_items)
                    rs.getInt("available_stock") // Buku yang tersedia (status available)
                };
                model.addRow(row);
                ditemukan = true;
            }

            if (!ditemukan) {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Untuk pencarian ID, masukkan angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Cari: " + e.getMessage());
        }
    }//GEN-LAST:event_button_cariActionPerformed

