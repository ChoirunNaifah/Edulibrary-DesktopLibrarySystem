    private void button_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_cariActionPerformed
        // TODO add your handling code here:
        String keyword = txtCari.getText().trim();

        if (keyword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan kata kunci pencarian!");
            return;
        }

        String sql;

        if (cmbCari.getSelectedIndex() == 0) {
            sql = "SELECT * FROM books WHERE id = ?";
        } else if (cmbCari.getSelectedIndex() == 1) {
            sql = "SELECT * FROM books WHERE title LIKE ?";
        } else {
            return;
        }

        try {
            Connection conn = koneksi.KoneksiDB();
            PreparedStatement pst = conn.prepareStatement(sql);

            if (cmbCari.getSelectedIndex() == 0) {
                pst.setInt(1, Integer.parseInt(keyword));
            } else {
                pst.setString(1, "%" + keyword + "%");
            }

            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblBuku.getModel();
            model.setRowCount(0);

            boolean ditemukan = false;

            while (rs.next()) {
                Object[] row = new Object[6];
                row[0] = rs.getInt("id");
                row[1] = rs.getString("title");
                row[2] = rs.getString("author");
                row[3] = "📷";
                row[4] = rs.getInt("stock");
                row[5] = rs.getInt("borrowed_times") + " Kali";
                model.addRow(row);
                ditemukan = true;
            }

            if (!ditemukan) {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!");
            }

            rs.close();
            pst.close();
            conn.close();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID harus berupa angka!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_button_cariActionPerformed

