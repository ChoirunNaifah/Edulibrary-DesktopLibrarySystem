    private void buttonFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonFilterActionPerformed
        // TODO add your handling code here:
        try {
            // Ambil nilai dari combobox
            int bulanIndex = cmbBulan.getSelectedIndex();
            String tahun = cmbTahun.getSelectedItem().toString();

            String query;

            // Cek apakah filter "Semua Bulan" dan "Semua Tahun" dipilih
            if (bulanIndex == 0 && tahun.equals("Semua Tahun")) {
                // Tampilkan semua data
                query = "SELECT * FROM visitors ORDER BY date DESC, time DESC";
            } else if (bulanIndex == 0) {
                // Filter hanya berdasarkan tahun
                query = "SELECT * FROM visitors WHERE YEAR(date) = " + tahun
                        + " ORDER BY date DESC, time DESC";
            } else if (tahun.equals("Semua Tahun")) {
                // Filter hanya berdasarkan bulan
                query = "SELECT * FROM visitors WHERE MONTH(date) = " + bulanIndex
                        + " ORDER BY date DESC, time DESC";
            } else {
                // Filter berdasarkan bulan dan tahun
                query = "SELECT * FROM visitors WHERE MONTH(date) = " + bulanIndex
                        + " AND YEAR(date) = " + tahun
                        + " ORDER BY date DESC, time DESC";
            }

            // Eksekusi query
            Connection conn = koneksi.KoneksiDB();
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            // Bersihkan tabel
            DefaultTableModel model = (DefaultTableModel) tblVisitor1.getModel();
            model.setRowCount(0);

            // Isi tabel dengan data hasil filter
            while (rs.next()) {
                Object[] row = new Object[5];
                row[0] = rs.getInt("id");
                row[1] = rs.getString("name");
                row[2] = rs.getString("classroom");
                row[3] = rs.getDate("date");
                row[4] = rs.getTime("time");
                model.addRow(row);
            }

            rs.close();
            stmt.close();
            conn.close();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(),
                    "Error Database",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonFilterActionPerformed

